function greet(restaurant) {

  const root = document.getElementById("root");
if(restaurant.length==0){
  const elementa=document.createElement("div");
  elementa.classList.add("asd");
  // root.style.backgroundColor="orange";
  root.style.marginTop="220px";
  elementa.textContent="Restaurant are out of stocks.  Try again after sometimes";
  elementa.style.fontSize="70px";
  root.appendChild(elementa);
}
else{
  restaurant.forEach((element) => {
    root.style.marginTop="0px";
      const card = document.createElement("div");
      card.classList.add("card");

      const image = document.createElement('img');
      image.src = element.image;
      card.appendChild(image);

      const CardContent = document.createElement("div");
      CardContent.classList.add("CardContent");

      const cardHeader = document.createElement("div");
      cardHeader.classList.add("CardHeader");

      const name1 = document.createElement("div");
      name1.classList.add("RestaurantName");
      name1.textContent = element.Name;

      const Rate = document.createElement("div");
      Rate.classList.add("Rate");
      Rate.textContent = `Ratings: ${element.Ratings}`;

      cardHeader.appendChild(name1);
      cardHeader.appendChild(Rate);

      const cardFooter = document.createElement("div");
      cardFooter.classList.add("CardFooter");

      const FoodTypes = document.createElement("div");
      FoodTypes.classList.add("FoodTypes");
      FoodTypes.textContent = element.FoodTypes;

      const price = document.createElement("div");
      price.classList.add("Price");
      price.textContent = `â‚¹${element.Price}`;

      cardFooter.appendChild(FoodTypes);
      cardFooter.appendChild(price);

      const cardDistance = document.createElement("div");
      cardDistance.classList.add("CardDistance");

      const location = document.createElement("div");
      location.classList.add("Location");
      location.textContent = element.Locations;

      const dis = document.createElement("div");
      dis.classList.add("Distance");
      dis.textContent = `${element.Distance} Km`;

      cardDistance.appendChild(location);
      cardDistance.appendChild(dis);

      const timing=document.createElement("div");
      timing.classList.add("Timing");

      const openingTiming=document.createElement("div");
      openingTiming.classList.add("Open");
      openingTiming.textContent=`Opening Timing:- ${element.OpenTimings} `;
      const closingTiming=document.createElement("div");
      closingTiming.classList.add("close");
      closingTiming.textContent=`Closing Timing:- ${element.CloseTimings} `;
      timing.appendChild(openingTiming);
      timing.appendChild(closingTiming);
      CardContent.appendChild(cardHeader);
      CardContent.appendChild(cardFooter);
      CardContent.appendChild(cardDistance);
      CardContent.appendChild(timing);

      card.appendChild(CardContent); // Append content to card
      root.appendChild(card); // Append the card to root
  });
}
}
